package AbstractFactoryPattern.Components;

public enum SocketType {
    // Intel Sockets
    LGA1200,      // Used for Intel's 10th and 11th generation CPUs
    LGA1700,      // Used for Intel's 12th and 13th generation CPUs

    // AMD Sockets
    AM4,          // Used for AMD Ryzen 1000 to Ryzen 5000 series CPUs
    AM5,          // Used for AMD Ryzen 7000 series
}
